# Déclaration et assignation des variables pour chaque type de données

# Entier (int)
int_value = 42

# Flottant (float)
float_value = 3.14

# Chaîne de caractères (str)
str_value = "Hello, World!"

# Valeur booléenne (bool)
bool_value = True

# Liste (list)
list_value = [1, 2, 3, 4, 5]

# Tuple (tuple)
tuple_value = (10, 20, 30)

# Dictionnaire (dict)
dict_value = {"name": "Alice", "age": 30, "city": "Paris"}

# Affichage des valeurs des variables
print(f"int_value : {int_value}")
print(f"float_value : {float_value}")
print(f"str_value : {str_value}")
print(f"bool_value : {bool_value}")
print(f"list_value : {list_value}")
print(f"tuple_value : {tuple_value}")
print(f"dict_value : {dict_value}")
