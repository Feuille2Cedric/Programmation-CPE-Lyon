# Déclaration et assignation des variables pour chaque type de données

# Entier (int)
int_value = 42  # Variable entière

# Flottant (float)
float_value = 3.14  # Variable flottante (nombre à virgule flottante)

# Chaîne de caractères (str)
str_value = "Hello, World!"  # Chaîne de caractères contenant un message de salutation

# Valeur booléenne (bool)
bool_value = True  # Variable booléenne (valeur True ou False)

# Liste (list)
list_value = [1, 2, 3, 4, 5]  # Liste contenant une série de nombres entiers

# Tuple (tuple)
tuple_value = (10, 20, 30)  # Tuple contenant trois entiers (un tuple est immuable)

# Dictionnaire (dict)
dict_value = {"name": "Alice", "age": 30, "city": "Paris"}  # Dictionnaire contenant des paires clé-valeur (nom, âge, ville)

# Affichage des valeurs des variables
print(f"int_value : {int_value}")  # Affiche la valeur de la variable entière
print(f"float_value : {float_value}")  # Affiche la valeur du flottant
print(f"str_value : {str_value}")  # Affiche la chaîne de caractères
print(f"bool_value : {bool_value}")  # Affiche la valeur booléenne
print(f"list_value : {list_value}")  # Affiche la liste
print(f"tuple_value : {tuple_value}")  # Affiche le tuple
print(f"dict_value : {dict_value}")  # Affiche le dictionnaire
